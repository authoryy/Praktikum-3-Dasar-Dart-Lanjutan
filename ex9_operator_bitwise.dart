void main(List<String> args) {
  int a = 120; // Deklarasi variabel a dengan nilai 120
  int b = 127; // Deklarasi variabel b dengan nilai 127
  
  print('Bitwise AND'); // Komentar untuk operasi bitwise AND
  
  print('$a & $b: ${a & b}'); // Operator bitwise & (AND) antara a dan b
  
  print('\nBitwise OR'); // Komentar untuk operasi bitwise OR
  
  print('$a | $b: ${a | b}'); // Operator bitwise | (OR) antara a dan b
  
  print('\nBitwise XOR'); // Komentar untuk operasi bitwise XOR
  
  print('$a ^ $b: ${a ^ b}'); // Operator bitwise ^ (XOR) antara a dan b
  
  print('\nBitwise NOT'); // Komentar untuk operasi bitwise NOT
  
  print('~$a: ${~a}'); // Operator bitwise ~ (NOT) untuk nilai a
  print('~$b: ${~b}'); // Operator bitwise ~ (NOT) untuk nilai b
  
  print('\nBitwise SHIFT LEFT'); // Komentar untuk operasi bitwise SHIFT LEFT
  
  print('$a << 1: ${a << 1}'); // Operator bitwise << (SHIFT LEFT) pada nilai a
  print('$b << 1: ${b << 1}'); // Operator bitwise << (SHIFT LEFT) pada nilai b
  
  print('\nBitwise SHIFT RIGHT'); // Komentar untuk operasi bitwise SHIFT RIGHT
  
  print('$a >> 1: ${a >> 1}'); // Operator bitwise >> (SHIFT RIGHT) pada nilai a
  print('$b >> 1: ${b >> 1}'); // Operator bitwise >> (SHIFT RIGHT) pada nilai b
}

