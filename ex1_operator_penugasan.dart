void main(List<String> args) {
  int a; // Deklarasi variabel a
  a = 3; // Operator penugasan (=) untuk memberikan nilai 3 ke variabel a
  print('a = $a'); // Menampilkan nilai a
  
  a += 2; // Operator penugasan (+=) untuk menambahkan 2 ke nilai a
  print('a = $a'); // Menampilkan nilai a
  
  a -= 1; // Operator penugasan (-=) untuk mengurangi 1 dari nilai a
  print('a = $a'); // Menampilkan nilai a
  
  a *= 2; // Operator penugasan (*=) untuk mengalikan nilai a dengan 2
  print('a = $a'); // Menampilkan nilai a
  
  a ~/= 3; // Operator penugasan (~/=) untuk melakukan pembagian bulat nilai a dengan 3
  print('a = $a'); // Menampilkan nilai a
  
  double b = 7; // Deklarasi variabel b dengan tipe data double
  b /= a; // Operator penugasan (/=) untuk membagi nilai b dengan nilai a
  print('b = $b'); // Menampilkan nilai b
}
