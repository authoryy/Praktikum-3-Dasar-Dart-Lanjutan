void main(List<String> args) {
  print('Logika AND'); // Komentar untuk operasi logika AND
  
  print('true && true\t: ${true && true}'); // Operator logika && (AND) untuk true dan true
  print('true && false\t: ${true && false}'); // Operator logika && (AND) untuk true dan false
  print('false && false\t: ${false && false}'); // Operator logika && (AND) untuk false dan false
  print('false && true\t: ${false && true}'); // Operator logika && (AND) untuk false dan true
  
  print('\nLogika OR'); // Komentar untuk operasi logika OR
  
  print('true || true\t: ${true || true}'); // Operator logika || (OR) untuk true atau true
  print('true || false\t: ${true || false}'); // Operator logika || (OR) untuk true atau false
  print('false || false\t: ${false || false}'); // Operator logika || (OR) untuk false atau false
  print('false || true\t: ${false || true}'); // Operator logika || (OR) untuk false atau true
  
  print('\nLogika NOT'); // Komentar untuk operasi logika NOT
  
  print('!true\t: ${!true}'); // Operator logika ! (NOT) untuk nilai true
  print('!false\t: ${!false}'); // Operator logika ! (NOT) untuk nilai false
}
