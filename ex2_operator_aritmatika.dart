void main(List<String> args) {
  int a = 10, b = 3; // Deklarasi variabel a dan b dengan nilai awal
  double c = 10.0; // Deklarasi variabel c dengan tipe data double

  print(
      '\n$a + $b = ${a + b}'); // Operator aritmatika penjumlahan (+) antara a dan b
  print(
      '$a - $b = ${a - b}'); // Operator aritmatika pengurangan (-) antara a dan b
  print(
      '$a * $b = ${a * b}'); // Operator aritmatika perkalian (*) antara a dan b
  print(
      '$a / $b = ${a / b}'); // Operator aritmatika pembagian (/) antara a dan b
  print(
      '$a ~/ $b = ${a ~/ b}'); // Operator aritmatika pembagian bulat (~/) antara a dan b
  print('$a % $b = ${a % b}'); // Operator aritmatika modulus (%) antara a dan b
}

