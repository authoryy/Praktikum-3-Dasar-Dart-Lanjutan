void main(List<String> args) {
  int a = 9, b = 10; // Deklarasi variabel a dan b dengan nilai awal
  
  print('$a == $b: ${a == b}'); // Operator relasional == untuk memeriksa apakah a sama dengan b
  print('$a != $b: ${a != b}'); // Operator relasional != untuk memeriksa apakah a tidak sama dengan b
  
  print('$a > $b: ${a > b}');      // Operator relasional > untuk memeriksa apakah a lebih besar dari b
  print('$a >= $b: ${a >= b}');   // Operator relasional >= untuk memeriksa apakah a lebih besar dari atau
                                 // sama dengan b
  print('$a < $b: ${a < b}');   // Operator relasional < untuk memeriksa apakah a kurang dari b
  print('$a <= $b: ${a <= b}'); // Operator relasional <= untuk memeriksa apakah a kurang dari atau sama
                                //dengan b
}
